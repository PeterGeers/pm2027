// Format amount for in spreadsheet
function format_amount($amount) {
	return '<right>'.html_entity_decode(PM_CURRENCY).' '.number_format($amount, 2, ',', '.').'</right>';
}

// Generate a worksheet, we can pass an extra function to calulate extra column.
function generate_worksheet($prefix, $columns, $submitted, $sql = null, $extra_function = null) {
	global $booking, $xlsx, $db, $clubs;
	// Set sql statement to default if not passed. We must use the submitted tables.
	$tab_name = $prefix;
	if ($submitted) { $tab_name .= SUBMITTED_POSTFIX; }
	$sql = ($sql) ? $sql : 'SELECT * FROM '.$tab_name.' ORDER BY `booking_id`;';
	// Build sql column names and sheet header row
	$row = array();
	foreach($columns as $column) {
		$row[] = '<b>'.ucfirst(str_replace('_', ' ', $column)).'</b>';
    // Insert Clubname after (booking_)id as applicable
    if (($prefix == BOOKING_PREFIX and $column == 'id') OR ($prefix != CONTACT_PREFIX and $column == 'booking_id')) {
      $row[] = '<b>Club Name</b>';
    }
	}
	$num_columns = count($row);
	$data[] = $row;
	$rows = 0;
	$db->query_columns($sql, $columns);
	if (!$db->error() AND $db->count() != 0) {
		$t_booking = new Booking();
		foreach ($db->results() as $idx => $item) {
			$itm = get_object_vars($item);
			$row = array();
			foreach($columns as $column) {
				if ($column == 'room_no') {
					$row[] = $booking->hotel_rooms->getHotelRoomByID($itm[$column]);
				} else {
					$row[] = $itm[$column];
				}
        // Add Club name after booking ID as applicable
        if (($prefix == BOOKING_PREFIX and $column == 'id') OR ($prefix != CONTACT_PREFIX and $column == 'booking_id')) {
          $row[] = getSaveArray($clubs, $itm[$column]);
        }
			}
			$data[] = $row;
			$rows++;
		}
	}
	$xlsx->addSheet($data, ucfirst($prefix));
	$xlsx->freezePanes('C2');
	$xlsx->autoFilter('A1:'.chr(64 + $num_columns)."{$rows}");
}

// Overview details as first worksheet
function generate_overview_worksheet($submitted) {
	global $booking, $xlsx, $user;
	$sheet_name = 'Overview';
	// Get all data and add to xlsx
	$booking_data = $booking->collectOverviewDetails(($submitted) ? 'Y':'N');
	$data = array();
	$data[] = array('<style font-size="32">Overview of booking data '.SITE_TITLE_SHORT.'</style>', null);
	$data[] = array('');
	$data[] = array('');
	$data[] = array('Generated on', date('d-M-Y'));
	$data[] = array('Generated by', $user->name());
	// Get statistics
	if ($submitted) {
		$data[] = array('Data set', 'Submitted bookings details only');
		$data[] = array('');
		$data[] = array('<b>Item</b>', '<b><right>Total of submitted</right></b>');
		$index = 2;
	} else {
		$data[] = array('Data set', 'All bookings details saved.');
		$data[] = array('');
		$data[] = array('<b>Item</b>', '<b><right>Total of saved</right></b>');
		$index = 1;
