	$data[] = array($booking_data[DELEGATE_PREFIX][0], $booking_data[DELEGATE_PREFIX][$index]);
	$data[] = array($booking_data[GUEST_PREFIX][0], $booking_data[GUEST_PREFIX][$index]);
	$data[] = array($booking_data[PARTY_PREFIX][0], $booking_data[PARTY_PREFIX][$index]);
	$data[] = array($booking_data[GUEST_PREFIX.'ct'][0], $booking_data[GUEST_PREFIX.'ct'][$index]);
	if ($booking->rooms->getEnabled()) {
		foreach(OPTIONS_ROOM_TYPES as $id => $name) {
			$idx = ROOM_PREFIX.$id;
			$data[] = array($booking_data[$idx][0], $booking_data[$idx][$index]);
		}
	}
	if ($booking->travels->getEnabled()) {
		$data[] = array($booking_data[TRAVEL_PREFIX.'arr'][0], $booking_data[TRAVEL_PREFIX.'arr'][$index]);
		$data[] = array($booking_data[TRAVEL_PREFIX.'dep'][0], $booking_data[TRAVEL_PREFIX.'dep'][$index]);
	}
	$data[] = array($booking_data[TSHIRT_PREFIX.'tot'][0], $booking_data[TSHIRT_PREFIX.'tot'][$index]);
	foreach(OPTIONS_TSHIRTS_SIZES as $id => $size) {
		if ($id != 'No shirt') {
			$idx = TSHIRT_PREFIX.str_replace(' ', '', $id);
			$data[] = array($booking_data[$idx][0], $booking_data[$idx][$index]);
		}
	}
	$data[] = array('');
	if ($submitted) {
		$data[] = array('The details worksheets show the submitted data, no unsubitted bookings or changes users may have made.');
	} else {
		$data[] = array('The details worksheets show the saved data, including changes users may have made after submitting their booking.');
	}
	$xlsx->addSheet($data, $sheet_name);
}

// Bookings summary overview worksheet
function generate_bookings_overview_worksheet($submitted) {
	global $booking, $xlsx, $db;
	$sheet_name = 'Bookings Overview';
	$table_postfix = '';
	if ($submitted) {
		$table_postfix = SUBMITTED_POSTFIX;
	}
	$data = array();
	$row = array();
	$row[] = '<b>Booking Ref</b>';
	$row[] = '<b>Club Name</b>';
	$row[] = '<b>User Name</b>';
	$row[] = '<b>Contact Name</b>';
	$row[] = '<b>Submitted</b>';
	$row[] = '<b># Delegates</b>';
	$row[] = '<b># Guests</b>';
	$row[] = '<b># Party</b>';
	$row[] = '<b># t-Shirts</b>';
	$row[] = '<b># City tour</b>';
	if ($booking->rooms->getEnabled()) {
		$row[] = '<b># Rooms</b>';
		if ($booking->travels->getEnabled()) {
			$row[] = '<b># Arr Trans.</b>';
			$row[] = '<b># Dep Trans.</b>';
		}
	}
	$row[] = '<b>Charges</b>';
	$row[] = '<b>Paid</b>';
	$row[] = '<b>Due</b>';
	$data[] = $row;
	$num_columns = count($row);

	// Get all bookings
	$sql = 'SELECT id, user_id, creation_date, modified_date, submitted_date, submitted_count, additional_descr, additional_costs, comments, is_locked FROM '.BOOKING_PREFIX.$table_postfix.' ORDER BY id;';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read booking data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$bookings = $db->results();
	// Get club and contact names
	$sql = 'SELECT booking_id, club_name, CONCAT(first_name, " ",last_name) as name FROM '.CONTACT_PREFIX.$table_postfix.';';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read booking contact data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$clubs = array();
	foreach($db->results() as $id => $item) {
		$clubs[$item->booking_id] = array($item->club_name, $item->name);
	}
	// Get users
	$sql = 'SELECT id, name FROM '.USERS_PREFIX.';';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read booking users data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$users = array();
	foreach($db->results() as $id => $item) {
		$users[$item->id] = $item->name;
	}
	// Get # Delegates
	$sql = 'SELECT booking_id, COUNT(*) AS count FROM '.DELEGATE_PREFIX.$table_postfix.' GROUP BY booking_id;';
	$db->query($sql);
	if ($db->error()) {
		$data[] = array('Unable to read booking delegates data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$delegates = array();
	foreach($db->results() as $id => $item) {
		$delegates[$item->booking_id] = $item->count;
	}
	// Get # Guests
	$sql = 'SELECT booking_id, COUNT(*) AS count FROM '.GUEST_PREFIX.$table_postfix.' GROUP BY booking_id;';
	$db->query($sql);
	if ($db->error()) {
		$data[] = array('Unable to read booking guests data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$guests = array();
	foreach($db->results() as $id => $item) {
		$guests[$item->booking_id] = $item->count;
	}
	// Get # party
	$sql = 'SELECT booking_id, COUNT(*) AS count FROM (SELECT booking_id, party FROM '.DELEGATE_PREFIX.$table_postfix.' UNION ALL SELECT booking_id, party FROM '.GUEST_PREFIX.$table_postfix.') AS combined_tables WHERE party = "Y" GROUP BY booking_id;';
	$db->query($sql);
	if ($db->error()) {
		$data[] = array('Unable to read booking party data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$party = array();
	foreach($db->results() as $id => $item) {
		$party[$item->booking_id] = $item->count;
	}
	// Get # t-Shirt
	$sql = 'SELECT booking_id, COUNT(*) AS count FROM (SELECT booking_id, shirt_size FROM '.DELEGATE_PREFIX.$table_postfix.' UNION ALL SELECT booking_id, shirt_size FROM '.GUEST_PREFIX.$table_postfix.') AS combined_tables WHERE shirt_size != "No shirt" GROUP BY booking_id;';
	$db->query($sql);
	if ($db->error()) {
		$data[] = array('Unable to read booking t-Shirt data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$tshirts = array();
	foreach($db->results() as $id => $item) {
		$tshirts[$item->booking_id] = $item->count;
	}
	// Get # City tour
	$sql =  'SELECT booking_id, COUNT(*) AS count FROM '.GUEST_PREFIX.$table_postfix.' WHERE city_tour = "Y" GROUP BY booking_id;';
	$db->query($sql);
	if ($db->error()) {
		$data[] = array('Unable to read booking city tour data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$citytours = array();
	foreach($db->results() as $id => $item) {
		$citytours[$item->booking_id] = $item->count;
	}
	if ($booking->rooms->getEnabled()) {
		// Get # Rooms
		$sql = 'SELECT booking_id, COUNT(*) AS count FROM '.ROOM_PREFIX.$table_postfix.' GROUP BY booking_id;';
		$db->query($sql);
		if ($db->error()) {
			$data[] = array('Unable to read booking rooms data');
			$xlsx->addSheet($data, $sheet_name);
			return false;
		}
		$rooms = array();
		foreach($db->results() as $id => $item) {
			$rooms[$item->booking_id] = $item->count;
		}
		if ($booking->travels->getEnabled()) {
			// Get # Travels
			$sql = 'SELECT booking_id, SUM(arr_amount) AS count FROM '.TRAVEL_PREFIX.$table_postfix.' WHERE arr_type = "PLANE" GROUP BY booking_id;';
			$db->query($sql);
			if ($db->error()) {
				$data[] = array('Unable to read booking travels data');
				$xlsx->addSheet($data, $sheet_name);
				return false;
			}
			$arr_trav = array();
			foreach($db->results() as $id => $item) {
				$arr_trav[$item->booking_id] = $item->count;
			}
			$sql = 'SELECT booking_id, SUM(dep_amount) AS count FROM '.TRAVEL_PREFIX.$table_postfix.' WHERE dep_type = "PLANE" GROUP BY booking_id;';
			$db->query($sql);
			if ($db->error()) {
				$data[] = array('Unable to read booking travels data');
				$xlsx->addSheet($data, $sheet_name);
				return false;
			}
			$dep_trav = array();
			foreach($db->results() as $id => $item) {
				$dep_trav[$item->booking_id] = $item->count;
			}
		}
	}
	$rows = 0;
	// Populate data
	$t_booking = new Booking;
	foreach ($bookings as $bid => $bkng) {
		$row = array();
		$id = $bkng->id;
		$row[] = number_format($id) . '-' . number_format($bkng->submitted_count);
		//$row[] = $t_booking->getReference($id, $bkng->submitted_count);
		if (array_key_exists($id, $clubs)) { $row[] = $clubs[$id][0]; } else { $row[]='?';}
		if (array_key_exists($bkng->user_id, $users)) { $row[] = $users[$bkng->user_id]; } else { $row[]='?';}
		if (array_key_exists($id, $clubs)) { $row[] = $clubs[$id][1]; } else { $row[]='?';}
		$row[] = $bkng->submitted_date;
		if (array_key_exists($id, $delegates)) {$row[] = $delegates[$id]; } else { $row[]='0';}
		if (array_key_exists($id, $guests)) {$row[] = $guests[$id]; } else { $row[]='0';}
		if (array_key_exists($id, $party)) {$row[] = $party[$id]; } else { $row[]='0';}
		if (array_key_exists($id, $tshirts)) {$row[] = $tshirts[$id]; } else { $row[]='0';}
		if (array_key_exists($id, $citytours)) {$row[] = $citytours[$id]; } else { $row[]='0';}
		if ($booking->rooms->getEnabled()) {
			if (array_key_exists($id, $rooms)) {$row[] = $rooms[$id]; } else { $row[]='0';}
			if ($booking->travels->getEnabled()) {
				if (array_key_exists($id, $arr_trav)) {$row[] = $arr_trav[$id]; } else { $row[]='0';}
				if (array_key_exists($id, $dep_trav)) {$row[] = $dep_trav[$id]; } else { $row[]='0';}
			}
		}
		// Add financials.
		$total = $t_booking->totalCharges($id, $submitted);
		$paid = $t_booking->totalPayments($id);
		$row[] = format_amount($total);
		$row[] = format_amount($paid);
		$row[] = format_amount($total - $paid);

		$data[] = $row;
		$rows++;
	}
	$xlsx->addSheet($data, $sheet_name);
	$xlsx->freezePanes('C2');
	$xlsx->autoFilter('A1:'.chr(64 + $num_columns)."{$rows}");
}

// details for reception/bags
function generate_reception_worksheet($submitted) {
	global $booking, $xlsx, $db, $clubs;
	$sheet_name = 'Bags';
	$table_postfix = '';
	if ($submitted) {
		$table_postfix = SUBMITTED_POSTFIX;
	}
	$data = array();
	$row = array();
	$row[] = '<b>Booking ID</b>';
	$row[] = '<b>Club Name</b>';
	$row[] = '<b>Name</b>';
	$row[] = '<b>Type</b>';
	$row[] = '<b>t-Shirt</b>';
	$row[] = '<b>Party</b>';
	$row[] = '<b>City Tour</b>';
	if ($booking->rooms->getEnabled()) {
		$row[] = '<b>Room type</b>';
		$row[] = '<b>Room number</b>';
	}
	$data[] = $row;
	$num_columns = count($row);
  // Get rooms details
	$sql = 'SELECT booking_id, type, room_no, guest1, guest2 FROM '.ROOM_PREFIX.$table_postfix.';';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read room data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$rooms = $db->results();
	$sql = 'SELECT DISTINCT d.booking_id, "Delegate" as dtype, last_name, CONCAT(first_name, " ",last_name) as name, shirt_size, party, "" as city_tour
		FROM '.DELEGATE_PREFIX.$table_postfix.' AS d
		UNION
		SELECT DISTINCT d.booking_id, "Guest" as dtype, last_name, CONCAT(first_name, " ",last_name) as name, shirt_size, party, city_tour
		FROM '.GUEST_PREFIX.$table_postfix.' AS d
		ORDER BY booking_id, last_name;';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read database.');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$rows = 0;
	foreach ($db->results() as $idx => $item) {
		$row = array();
		$row[] = $item->booking_id;
		$row[] = getSaveArray($clubs, $item->booking_id); // Club name
		$row[] = $item->name;
		$row[] = $item->dtype;
		$row[] = $item->shirt_size;
		$row[] = $item->party;
		$row[] = $item->city_tour;
		if ($booking->rooms->getEnabled()) {
      foreach($rooms as $id => $room) {
        if ($item->booking_id == $room->booking_id) {
          if ((isset($room->guest1) AND (strtolower($room->guest1) == strtolower($item->name))) OR (isset($room->guest2) and (strtolower($room->guest2) == strtolower($item->name)))) {
            $row[] = $room->type;
            $row[] = $booking->hotel_rooms->getHotelRoomByID($room->room_no);
          }
        }
      }
		}
		$data[] = $row;
		$rows++;
	}
	$xlsx->addSheet($data, $sheet_name);
	$xlsx->freezePanes('C2');
	$xlsx->autoFilter('A1:'.chr(64 + $num_columns)."{$rows}");
}

