// Generate worksheet listing all known FH-DCE clubs and if they have a booking
function generate_fhdce_clubs_worksheet($submitted){
	global $xlsx, $db;
	$sheet_name = 'FH-DCE Clubs';
	$table_postfix = '';
	if ($submitted) {
		$table_postfix = SUBMITTED_POSTFIX;
	}
	$data = array();
	$data[] = array('Club ID', 'Club Name', 'CC', 'Booking_ID', 'Submitted Date');
	$sql = 'SELECT id, name, cc FROM '.FHDCE_CLUBS_PREFIX.' ORDER BY name;';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read club data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$fh_clubs = $db->results();
	$sql = 'SELECT id, submitted_date FROM '.BOOKING_PREFIX.$table_postfix.';';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read booking data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$bookings = $db->results();
	$sql = 'SELECT booking_id, club_name FROM '.CONTACT_PREFIX.$table_postfix.';';
	$db->query($sql);
	if ($db->error() OR $db->count() == 0) {
		$data[] = array('Unable to read booking data');
		$xlsx->addSheet($data, $sheet_name);
		return false;
	}
	$contacts = $db->results();
	$rows = 0;
	$num_columns = 6;
	foreach ($fh_clubs as $idx => $item) {
		// Find club booking using contacts
		$booking_id = null;
		$sub_date = null;
		foreach ($contacts as $idxx => $contact) {
			if ($contact->club_name == $item->name) {
				$booking_id = $contact->booking_id;
				break;
			}
		}
		if ($booking_id != null) {
			foreach ($bookings as $idxx => $booking) {
				if ($booking->id == $booking_id) {
					$sub_date = $booking->submitted_date;
					break;
				}
			}
		}
		$row = array();
		$row[] = $item->id;
		$row[] = $item->name;
		$row[] = $item->cc;
		$row[] = $booking_id;
		$row[] = $sub_date;
		$data[] = $row;
		$rows++;
	}
	
	$xlsx->addSheet($data, $sheet_name);
	$xlsx->freezePanes('B2');
	$xlsx->autoFilter('A1:'.chr(64 + $num_columns)."{$rows}");
}

